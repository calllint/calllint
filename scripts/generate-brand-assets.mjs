#!/usr/bin/env node
/**
 * Generate web/report-sized brand assets from assets/calllintlogo.png.
 * Requires: npx sharp (dev-only, not a runtime dependency).
 */
import { copyFile, mkdir, readFile, stat, writeFile } from "node:fs/promises"
import { join, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const root = join(dirname(fileURLToPath(import.meta.url)), "..")
const src = join(root, "assets/calllintlogo.png")
const outDir = join(root, "assets/brand")

const SIZES = [
  ["favicon-32.png", 32],
  ["logo-mark-64.png", 64],
  ["logo-mark-128.png", 128],
  ["logo-mark-256.png", 256],
  ["logo-og-512.png", 512],
  ["logo-report-48.png", 48],
  ["github-org-avatar.png", 256],
  ["npm-org-avatar.png", 260],
]

async function main() {
  const sharp = (await import("sharp")).default
  await mkdir(outDir, { recursive: true })

  for (const [name, size] of SIZES) {
    const out = join(outDir, name)
    await sharp(src)
      .resize(size, size, { fit: "contain", background: { r: 0, g: 0, b: 0, alpha: 0 } })
      .png({ compressionLevel: 9, palette: true })
      .toFile(out)
    const { size: bytes } = await stat(out)
    console.log(`${name}\t${bytes} bytes`)
  }

  const report = await readFile(join(outDir, "logo-report-48.png"))
  const b64 = report.toString("base64")
  await writeFile(join(outDir, "logo-report-base64.txt"), b64)

  const ts = `/** Generated by scripts/generate-brand-assets.mjs — do not edit by hand. */
export const LOGO_REPORT_BASE64 = ${JSON.stringify(b64)}
`
  await writeFile(join(root, "packages/report-renderer/src/logoBase64.ts"), ts)
  console.log(`logo-report-base64\t${b64.length} chars`)

  // Keep legacy root filename in sync (GitHub/npm manual uploads).
  await copyFile(
    join(outDir, "logo-mark-256.png"),
    join(root, "assets/calllint-org-avatar.png"),
  )
  console.log("calllint-org-avatar.png\t(copied from logo-mark-256.png)")
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
